const express = require('express'),
    app = express(),
    cors = require('cors'),
    bodyParser = require('body-parser'),
    mongoose = require('mongoose'),
    Blog = require("./models/Blog"),
    seedDb = require("./seeds"),
    methodOverride = require('method-override');


//Mongoose Config
const mongooseUrl = "mongodb://localhost:27017/myblog"
// start the mongo server in another bash by ./mongod command
//connecting mongoose 
mongoose.connect(mongooseUrl, {
    useNewUrlParser: true,
    useCreateIndex: true,
    useUnifiedTopology: true,
    useFindAndModify: false
});
// seedDb();

//App config

app.use(express.static(__dirname + "/public"));
app.set("view engine", "ejs");
app.use(bodyParser.urlencoded({ extended: true }));
app.use(cors());
app.use(methodOverride("_method"));



app.get("/", (req, res) => {
    res.redirect("/blog/index");
});

app.get("/blog", (req, res) => {
    res.redirect("/blog/index");
});


// =====================================================
// ROUTE 
// =====================================================

// INDEX ROUTE 
app.get("/blog/index", (req, res) => {
    Blog.find({}, (err, blogs) => {

        if (err) {
            res.status(400).json(err);
        }
        if (!blogs) {
            res.status(404).json({ message: 'Blogs not found.' });
        }
        res.render("index", { blogs: blogs });
        // res.json(blogs);
    });


});
// NEW ROUTE 
app.get("/blog/new", (req, res) => {
    res.render("new");
});

//NEW FORM
app.post("/blog/new", (req, res) => {
    // console.log("NEW Blog");
    // console.log(newBlog);
    Blog.create(req.body.blog, (err, blog) => {
        if (err) {
            console.log(err);
        } else {
            console.log("Blog Added.........");
            //console.log(blog);
            res.redirect('/blog/index');
        }
    });


});

// SHOW PAGE 
app.get("/blog/:id", (req, res) => {
    const _id = req.params.id;
    //Find the user by id
    //then render the show page
    Blog.findOne({ _id }, (err, blog) => {
        if (err) {
            res.status(400).json(err);
        }
        if (!blog) {
            res.status(404).json({ message: 'Blog not found.' });
        }
        res.render("show", { blog: blog });
        // res.json(blog);
    });

});


//EDIT PAGE
app.get("/blog/:id/edit", (req, res) => {
    //Find the post by edit & then show the values
    Blog.findById(req.params.id, (err, blog) => {
        if (err) {
            res.status(400).json(err);
        }
        res.render("edit", { blog: blog });

    });


});

//UPDATE 
app.put("/blog/:id", (req, res) => {

    Blog.findByIdAndUpdate(req.params.id, req.body.blog, { new: true }, (err, updateBlog) => {
        if (err) {
            res.status(400).json(err);
        }
        res.redirect(`/blog/${req.params.id}`);
    });
});

// Setting up port 
app.listen(3000, () => {
    console.log("Blog Site Server started");
});